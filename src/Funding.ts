/**
 * Up-to-date information about ways to help fund development of your package.
 */
export default interface Funding {
  /**
   * Funding type.
   * @example individual
   * @example patreon
   */
  type: string;
  /**
   * Funding url.
   * @example http://example.com/donate
   * @example https://www.patreon.com/my-account
   */
  url: string;
}
